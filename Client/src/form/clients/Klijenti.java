    void populateCmb() {
        
        try {
            klijenti=Controller.getInstance().getKlijenti();
            cmbKlijent.removeAllItems();
            for (Klijent klijent : klijenti) {
                cmbKlijent.addItem(klijent);
            }
        } catch (Exception ex) {
            Logger.getLogger(Klijenti.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
