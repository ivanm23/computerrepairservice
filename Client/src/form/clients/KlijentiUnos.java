    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed

        String ime = txtIme.getText().trim();
        String prezime = txtPrezime.getText().trim();
        String brTel = txtBrTel.getText().trim();

        if (ime.isEmpty() && prezime.isEmpty() && brTel.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Popunite sva polja!", "Greska", JOptionPane.ERROR_MESSAGE);
        }

        Klijent klijentPop = getPopulatedClient();
        if (klijentPop != null) {

            try {
                if (this.klijent != null) {
                    klijent.setKlijentIme(ime);
                    klijent.setKlijentPrezime(prezime);
                    klijent.setKlijentBrTel(brTel);
                    Controller.getInstance().updateClient(klijent);
                    if (parentKlijenti==null) {
                        parentKonfiguracija.populateCmb();
                    }else parentKlijenti.populateCmb();
                    JOptionPane.showMessageDialog(this, "Klijent je uspešno izmenjen!", "Izmena klijenta", JOptionPane.INFORMATION_MESSAGE);

                    int result = JOptionPane.showConfirmDialog(this, "Napravite još izmena?", "Upit", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
                    if (result == 0) {
                        txtIme.setText("");
                        txtPrezime.setText("");
                        txtBrTel.setText("");
                    }else 
                        dispose();
                } else {
                    Controller.getInstance().addClient(klijentPop);
                    if (parentKlijenti==null) {
                        parentKonfiguracija.populateCmb();
                    }else parentKlijenti.populateCmb();
                    JOptionPane.showMessageDialog(this, "Klijent je uspešno sačuvan!", "Dodavanje klijenta", JOptionPane.INFORMATION_MESSAGE);

                    int result = JOptionPane.showConfirmDialog(this, "Dodajte novog klijenta?", "Upit", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
                    if (result == 0) {
                        txtIme.setText("");
                        txtPrezime.setText("");
                        txtBrTel.setText("");
                    }else 
                        dispose();
                }

            } catch (Exception ex) {
                Logger.getLogger(KlijentiUnos.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_btnSaveActionPerformed

