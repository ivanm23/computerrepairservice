    private Klijent getPopulatedClient() {
        try {
            String ime = txtIme.getText().trim();
            String prezime = txtPrezime.getText().trim();
            String brTel = txtBrTel.getText().trim();

            if (ime.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Polje za ime ne sme biti prazno!", "Greska", JOptionPane.ERROR_MESSAGE);
                return null;
            }
            if (prezime.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Polje za prezime ne sme biti prazno!", "Greska", JOptionPane.ERROR_MESSAGE);
                return null;
            }
            if (brTel.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Polje za broj telefona ne sme biti prazno!", "Greska", JOptionPane.ERROR_MESSAGE);
                return null;
            }
            Klijent klijentPop = new Klijent(Controller.getInstance().getClientLastID(), ime, prezime, brTel, new LinkedList<>());
            return klijentPop;
        } catch (Exception ex) {
            Logger.getLogger(KlijentiUnos.class.getName()).log(Level.SEVERE, null, ex);
        }
        return null;
    }
